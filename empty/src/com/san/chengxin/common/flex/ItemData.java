package com.san.chengxin.common.flex;

/**
 * 下拉列表
 * @author shmilyLT
 *
 */
public class ItemData {
	
	/** 列表中的显示项 */
	private String label;
	
	/** 列表中的值 */
	private String data;
	
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	
	
}
